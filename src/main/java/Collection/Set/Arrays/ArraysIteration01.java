package Collection.Set.Arrays;

// Проход по массиву через while
public class ArraysIteration01 {

    public static void main(String[] args) {

        int[] nums = {8, 12, 3, 9};

        int i = 0;

        while (i < nums.length) {
            System.out.println(nums[i]);
            i++;
        }
    }
}
