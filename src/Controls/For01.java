package Controls;

// Все значения и условия в for()
public class For01 {

    public static void main(String[] args) {

        // Базовый цикл for.
        // ++ - инкремент (увеличение на единицу).
        for (int a = 1; a < 5; a++) {
            System.out.println("Our value: " + a);
        }
    }
}
